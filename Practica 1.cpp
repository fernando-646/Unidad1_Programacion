#include <iostream>
using namespace std;

int main() {
    int A, B, C, D;
    
    cout << "Ingresa los datos que quiera invertir " << "\n";
    cout << "Porfavor ingrese el valor de A" << "\n";
    cin >> A;
    cout << "Porfavor ingrese el valor de B" << "\n";
    cin >> B;
    cout << "Porfavor ingrese el valor de C" << "\n";
    cin >> C;
    cout << "Porfavor ingrese el valor de D" << "\n";
    cin >> D;
    cout << "Los numeros que ingreso pero en orden inverso son: " << D << " " << C << " " << B << " " << A << "\n";
    return 0;
}

